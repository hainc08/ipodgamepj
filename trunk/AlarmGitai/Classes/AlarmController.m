-(IBAction)toggleCancle:(id)sender {
	[super.navigationController.view removeFromSuperview  ];
}
- (void)addItem:sender {
    if (alarmconfigConroller == nil) {
        alarmconfigConroller = [[AlarmConfigController alloc] init];
    }
    UINavigationController *navigationController = [[UINavigationController alloc] initWithRootViewController:alarmconfigConroller];
    [[self navigationController] presentModalViewController:navigationController animated:YES];
    [navigationController release];
}

- (void)viewDidLoad {
    [super viewDidLoad];
	self.title = @"Alarm";
	
	
	UIBarButtonItem *closeButton = [[UIBarButtonItem alloc]
									initWithTitle:@"Close"
									style:UIBarButtonItemStyleBordered
									target:self
									action:@selector(toggleCancle:)];
    self.navigationItem.leftBarButtonItem	= closeButton;

	addButton = [[UIBarButtonItem alloc] initWithBarButtonSystemItem:UIBarButtonSystemItemAdd target:self action:@selector(addItem:)];
    self.navigationItem.rightBarButtonItem = addButton;

}


